class product{
    // atribut
    private int price;
    private String idProduct;

    // constructor kosong
    product(){}    

    // Getter dan Setter

    public int getPrice() {
        return price;
    }
    public void setPrice(int price) {
        this.price = price;
    }

    public String getIdProduct() {
        return idProduct;
    }
    public void setIdProduct(String idProduct) {
        this.idProduct = idProduct;
    }

   
}